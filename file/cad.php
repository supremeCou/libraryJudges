<div id="content_1" style="vertical-align: top; text-align: justify;"><span style="font-family: calibri;"><big>

    The Constituent Assembly met for the first time in New Delhi on 9 December, 1946 in the Constitution Hall which is now known as the Central Hall of Parliament House.
The Constituent Assembly took almost three years (two years, eleven months and seventeen days to be precise) to complete its historic task of drafting the Constitution for Independent India. During this period, it held 11 sessions covering a total of 165 days. Of these, 114 days were spent on the consideration of the Draft Constitution. The Constitution of India was adopted on November 26, 1949. In all 284 members actually signed the Constitution on January 24, 1950.The Constitution came into force on January 26, 1950 and the Constituent Assembly was dissolved.
In December 2021, it will be 75 years of the first meeting of the Constituent Assembly.</big>
